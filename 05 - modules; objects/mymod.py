# mymod.py  A very simple illustration of how to create your own module.

# Modules essentially stand for your own custom libraries.
# One might want to use the same functions, constants or classes in different xp
# e.g: operations or experimental paradigms.
# Instead of using several definitions of the same functions across many scripts,
# one can define them only once in a "module" and import them.

# define a function
def addit(a=10, b=20):
    return a+b

# define a constant
earth_radius_km = 12742

